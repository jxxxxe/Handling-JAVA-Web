package sec01.ex02;

import java.io.IOException;
import java.io.PrintWriter;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

@WebServlet("/second")
public class SecondServlet extends HttpServlet {


	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		request.setCharacterEncoding("utf-8");
		response.setContentType("text/html;charset=utf-8");
		PrintWriter out=response.getWriter();
		
		String user_id=request.getParameter("user_id"); // ?로 구분되어 보낸것이 name-value가 됨
		String user_pw=request.getParameter("user_pw");
		String user_address=request.getParameter("user_address");
		
		if(user_id!=null &&user_id.length()!=0) {
			String data="이미 로그인 상태입니다!<br><br>";
			data+="<html><body>";
			data+="첫 번째 서블릿에서 넘겨준 아이디 : "+user_id;
			data+="<br>첫 번째 서블릿에서 넘겨준 비밀번호 : "+user_pw;
			data+="<br>첫 번째 서블릿에서 넘겨준 주소 : "+user_address;
			data+="</html></body>";
			
			out.print(data);
		}else {
			out.print("로그인 하지 않았습니다.");
			out.print("다시 로그인하세요!!");
			out.print("<a href='/pro09/login.html'> 로그인 창으로 이동하기</a>");

		}
	}
}
